#!/usr/bin/env python3

#Declaring a list
my_list = ['jordan', 'ellesse','nike','adidas','Vans off the wall','Off white' 'Cactus Jack', 'Palm angles','Dior','Essentials fear of god']
print(my_list)

#Accessing the list elements with indexing
print(my_list[1])

