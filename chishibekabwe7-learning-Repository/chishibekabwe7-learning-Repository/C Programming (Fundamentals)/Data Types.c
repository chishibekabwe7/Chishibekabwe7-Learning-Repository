#include <stdio.h>
int main () {
    //integars
    int myInt = 24;
    printf("%i\n",myInt);

    //floats
    float myFlaot = 24.0;
    printf("%f\n",myFlaot);

    //Doubles
    double myDouble = 24.24;
    printf("%lf\n",myDouble);

    //Characters
    char myCharacter = 'C';
    printf("%c\n",myCharacter);

    //Strings
    char myString[] = "Chishibe Kabwe";
    printf("%s\n",myString);

}